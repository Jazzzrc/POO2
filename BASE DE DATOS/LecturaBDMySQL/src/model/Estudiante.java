package model;

/**
 *
 * @author Favio
 */
public class Estudiante {
    private int id;
    private String codigo;
    private String nombre;
    private String curso;

    public Estudiante(int id, String codigo, String nombre, String curso) {
        this.id = id;
        this.codigo = codigo;
        this.nombre = nombre;
        this.curso = curso;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }
    
    
}
